/**
 * Time complexity:O();
 * Space complexity: O();
 * Description: TODO
 */
public class ListNode {
        int val;
        ListNode next;

        ListNode() {
        }

        ListNode(int val) {
            this.val = val;
        }

        ListNode(int val, ListNode next) {
            this.val = val;
            this.next = next;
        }
}
